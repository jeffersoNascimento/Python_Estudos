# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo.
print(11)  # int
print(-11) # int (tipos primitivos)


# float -> Número com ponto flutuante 
# O tipo float representa qualquer numero positivo ou negativo real.
# float sem sinal é considerado positivo
print(1.1)       # float
print(2.3, -1.5) # float

# A função type mostra o tipo que o Python inferiu a valor
print(type(123))
print(type(1.23))
print(type("123"))
print(type("123"), type(123), type(1.23))
