"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
# Aspas simples
print('aspas simples')

# Aspas duplas
print("Aspas Duplas")

# Escape (Caracteri de escape)
print("Jeff \"Nascimento\"")

# r (expressões regulares)
print(r"Jeff \"Nascimento\"")

print('Jeff "Nascimento"') # Forma mais simples

